sql_file = "/home/skyrius/habiDue/plan/all_korea_regions.sql"
processed_keywords = set()  # 중복 제거용 집합

with open(sql_file, "w", encoding="utf-8") as f:
    f.write("-- [최종 고도화] 전국 행정구역 메타데이터 중복 제로 SQL\n")
